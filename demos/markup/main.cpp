bool GetSubDocsWithPathElem(const std::string &xml_str, const std::vector<std::string> &path_item, std::vector<std::string> &res) {
    auto item = path_item.back();
    bl::BlMarkup xml;
    if (!xml.SetDoc(xml_str)) {
        return false;
    }
    xml.ResetPos();

    auto iterCnt = path_item.size() - 1;
    for (int i = 0; i < iterCnt; i++) {
        if (!xml.FindElem(path_item[i])) {
            return false;
        }
        xml.IntoElem();
    }

    std::vector<std::string> r;

    while (xml.FindElem(item)) {
        r.push_back(xml.GetSubDoc());
    }

    for (int i = 0; i < iterCnt; i++) {
        xml.OutOfElem();
    }

    res.swap(r);

    return true;
}

bool GetSubDocWithPathElem(const std::string &xml_str, const std::vector<std::string> &path_item, std::string &res) {
    std::vector<std::string> v;
    if (!GetSubDocsWithPathElem(xml_str, path_item, v)) {
        return false;
    }

    res = v[0];

    return true;
}

