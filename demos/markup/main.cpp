bool GetDataWithPathElem(const std::string &xml_str, const std::vector<std::string> &path_item, std::string &res) {
    auto item = path_item.back();

    bl::BlMarkup xml;
    if (!xml.SetDoc(xml_str)) {
        return false;
    }
    xml.ResetPos();

    auto iterCnt = path_item.size() - 1;
    for (int i = 0; i < iterCnt; i++) {
        if (!xml.FindElem(path_item[i])) {
            return false;
        }
        xml.IntoElem();
    }

    if (!xml.FindElem(item)) {
        return false;
    }

    res = xml.GetData();

    for (int i = 0; i < iterCnt; i++) {
        xml.OutOfElem();
    }

    return true;
}

bool GetDataWithPathElem(const std::string &xml_str, const std::vector<std::string> &path, const std::string &elem, std::string &res) {
    std::vector<std::string> path_elem{path};
    path_elem.push_back(elem);

    return GetDataWithPathElem(xml_str, path_elem, res);
}

bool SetDataWithPathElem(const std::vector<std::string> &path_item, const std::string &value, std::string &res) {
    if (path_item.empty()) {
        return false;
    }
    bl::BlMarkup xml;
    if (1 == path_item.size()) {
        xml.AddElem(path_item.back(), value);
        res = xml.GetDoc();
        return true;
    }
    auto iter = path_item.begin();
    xml.AddElem(*iter);
    for (int i = 1; i < path_item.size() - 1; i++) {
        xml.AddChildElem(*iter);
        xml.IntoElem();
    }
    xml.AddChildElem(path_item.back(), value);
    res = xml.GetDoc();

    return true;
}


